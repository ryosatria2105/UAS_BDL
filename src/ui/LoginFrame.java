    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
      String username = txtUsername.getText().trim();
    String password = new String(txtPassword.getPassword());
    String role = cbRole.getSelectedItem().toString(); // Admin / Kasir

    if (username.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Username dan password wajib diisi!");
        return;
    }

    PegawaiService service = new PegawaiService();
    Pegawai p = service.login(username, password, role);

    if (p == null) {
        JOptionPane.showMessageDialog(this, "Username / Password / Role salah!");
        return;
    }

    JOptionPane.showMessageDialog(this, "Login sukses, " + p.getNama());

    if (role.equalsIgnoreCase("Admin")) {
        new AdminDashboard(p.getId(), p.getNama()).setVisible(true);


} else {
    new KasirDashboard(p.getId(), p.getNama()).setVisible(true);
}

    this.dispose();
    }//GEN-LAST:event_btnLoginActionPerformed

