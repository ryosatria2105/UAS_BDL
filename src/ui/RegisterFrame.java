    private void btnDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDaftarActionPerformed
     Pegawai p = new Pegawai();

    p.setNama(txtNama.getText());
    p.setUsername(txtEmail.getText());
    p.setPassword(txtPassword.getText());
    p.setJabatan(cbRole.getSelectedItem().toString());

    PegawaiService service = new PegawaiService();

    if (service.isUsernameExist(p.getUsername())) {
        JOptionPane.showMessageDialog(this, "Username sudah digunakan!");
        return;
    }

    boolean ok = service.register(p);

    if (ok) {
        JOptionPane.showMessageDialog(this, "Registrasi berhasil!");
        this.dispose();
        new LoginFrame().setVisible(true);
    } else {
        JOptionPane.showMessageDialog(this, "Registrasi gagal!");
    }
    }//GEN-LAST:event_btnDaftarActionPerformed

